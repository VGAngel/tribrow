    public PerlinNoise(int seed) {
        p = new int[514];
        g = new double[514][3];
        setSeed(seed);
    }

    public void setSeed(int seed) {
        this.seed = seed;
        initNoise();
    }

    public int getSeed() {
        return seed;
    }

    private void initNoise() {
        Random rand = new Random(seed);
        int i;
        for (i = 0; i < 256; i++) {
            p[i] = i;
            for (int j = 0; j < 3; j++)
                g[i][j] = (double) (rand.nextInt(512) - 256) / 256D;

            normalize3(g[i]);
        }

        while (--i != 0) {
            int k = p[i];
            int j = rand.nextInt(256);
            p[i] = p[j];
            p[j] = k;
        }
        for (i = 0; i < 258; i++) {
            p[256 + i] = p[i];
            for (int j = 0; j < 3; j++)
                g[256 + i][j] = g[i][j];

        }

    }

    public static double s_curve(double t) {
        return t * t * (3D - 2D * t);
    }

    private static double lerp(double t, double a, double b) {
        return a + t * (b - a);
    }

    private static double at3(double rx, double ry, double rz, double q[]) {
        return rx * q[0] + ry * q[1] + rz * q[2];
    }

    private static double at2(double rx, double ry, double q[]) {
        return rx * q[0] + ry * q[1];
    }

    private static void normalize3(double v[]) {
        double s = Math.sqrt(v[0] * v[0] + v[1] * v[1] + v[2] * v[2]);
        v[0] = v[0] / s;
        v[1] = v[1] / s;
        v[2] = v[2] / s;
    }

    public final double getNoise(double x, double y, double z) {
        double t = x + 4096D;
        int bx0 = (int) t & 0xff;
        int bx1 = bx0 + 1 & 0xff;
        double rx0 = t - (double) (int) t;
        double rx1 = rx0 - 1.0D;
        t = y + 4096D;
        int by0 = (int) t & 0xff;
        int by1 = by0 + 1 & 0xff;
        double ry0 = t - (double) (int) t;
        double ry1 = ry0 - 1.0D;
        t = z + 4096D;
        int bz0 = (int) t & 0xff;
        int bz1 = bz0 + 1 & 0xff;
        double rz0 = t - (double) (int) t;
        double rz1 = rz0 - 1.0D;
        int i = p[bx0];
        int j = p[bx1];
        int b00 = p[i + by0];
        int b10 = p[j + by0];
        int b01 = p[i + by1];
        int b11 = p[j + by1];
        double sx = s_curve(rx0);
        double sy = s_curve(ry0);
        double sz = s_curve(rz0);
        double u = at3(rx0, ry0, rz0, g[b00 + bz0]);
        double v = at3(rx1, ry0, rz0, g[b10 + bz0]);
        double a = lerp(sx, u, v);
        u = at3(rx0, ry1, rz0, g[b01 + bz0]);
        v = at3(rx1, ry1, rz0, g[b11 + bz0]);
        double b = lerp(sx, u, v);
        double c = lerp(sy, a, b);
        u = at3(rx0, ry0, rz1, g[b00 + bz1]);
        v = at3(rx1, ry0, rz1, g[b10 + bz1]);
        a = lerp(sx, u, v);
        u = at3(rx0, ry1, rz1, g[b01 + bz1]);
        v = at3(rx1, ry1, rz1, g[b11 + bz1]);
        b = lerp(sx, u, v);
        double d = lerp(sy, a, b);
        return 1.5D * lerp(sz, c, d);
    }

    public final double getNoise(double x, double y) {
        double t = x + 4096D;
        int bx0 = (int) t & 0xff;
        int bx1 = bx0 + 1 & 0xff;
        double rx0 = t - (double) (int) t;
        double rx1 = rx0 - 1.0D;
        t = y + 4096D;
        int by0 = (int) t & 0xff;
        int by1 = by0 + 1 & 0xff;
        double ry0 = t - (double) (int) t;
        double ry1 = ry0 - 1.0D;
        int i = p[bx0];
        int j = p[bx1];
        int b00 = p[i + by0];
        int b10 = p[j + by0];
        int b01 = p[i + by1];
        int b11 = p[j + by1];
        double sx = s_curve(rx0);
        double sy = s_curve(ry0);
        double u = at2(rx0, ry0, g[b00]);
        double v = at2(rx1, ry0, g[b10]);
        double a = lerp(sx, u, v);
        u = at2(rx0, ry1, g[b01]);
        v = at2(rx1, ry1, g[b11]);
        double b = lerp(sx, u, v);
        return 1.5D * lerp(sy, a, b);
    }

